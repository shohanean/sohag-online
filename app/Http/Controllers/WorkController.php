     */
    public function update(Request $request, Work $work)
    {
        $work->user_id = $request->user_id;
        if ($request->user_id) {
            $work->status = "running";
        }else{
            $work->status = "open";
        }
        $work->save();
        return back()->with('update_success', 'Updated Successfully!');
